#include "libft.h"

int		ft_count_num(long int n)
{
	int tmp;
	int tmp2;

	tmp2 = (int)n;
	tmp = 1;
	if (n < 0)
		++tmp;
	while ((tmp2 = tmp2 / 10))
		++tmp;
	return (tmp);
}
